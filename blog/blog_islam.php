                                                                                <p>कुरान के चैप्टर 23 की आयत नंबर 115 में वर्णित है कि अल्लाह ने जो कुछ भी इस दुनिया
में बनाया है वह एक दिन उसे मिटाएगा जरूर । इस दिन को 'अल साआह' यानी कयामत
वाला दिन कहा गया है। कान में यह लिखा हुआ है कि अल्लाह कयामत के दिन के आने से
पहले हमें इसके अनेकों संकेत भी देगा।
जैसे कि पृथ्वी पर भूचाल आएंगे, आसमान में धुंए के काले घने बादल छा जाएंगे, पहाड़ टूट
कर मिट्टी में बदल जाएंगे। कयामत वाले दिन से ठीक 1 दिन पहले मक्का और काबा भी
नष्ट हो जाएगा । चैप्टर 39 की आयत नंबर 68 में यह बताया गया है कि आसमान से एक
शंखनाद की आवाज आएगी जिसे सुनकर पृथ्वी पर सभी लोग मर जाएंगे, सिवाय उनके
