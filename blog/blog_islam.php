चैप्टर 50 की आयत नंबर 24 में यह वर्णित है कि जो लोग अल्लाह पर विश्वास नहीं करते
उन्हें अल्लाह नरक में भेजने का आदेश दे देता है। इस प्रक्रिया में व्यक्ति के बचपन में किए हुए कर्मों को नहीं गिना जाता। इसके अतिरिक्त जो
व्यक्ति मोहम्मद साहब के दुनिया में आने से पहले ही मर चुके थे जिस कारण उन्हें इस्लाम
धर्म का पैगाम ही नहीं मिल पाया उन्हें भी अल्लाह माफ कर देता है ।
हदीस में यह भी वर्णित है कि जो लोग शहादत को प्राप्त होते हैं उन्हें मरने के पश्चात कब्र
में नहीं जाना पड़ता, और कयामत वाले दिन उन्हें नाम मात्र को सजा देकर (थोड़ा सा खून
बहते ही) अल्लाह उन्हें माफ करके तुरंत स्वर्ग में भेज देता है। इसके अतिरिक्त हदीस में यह
