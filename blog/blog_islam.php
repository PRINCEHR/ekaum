पुराने शरीर के साथ रहना पड़ता है।
इस्लाम (और ईसाई) धर्म में माना जाता है कि अंत में एक दिन ऐसा आएगा जिसे 'कयामत का दिन'
(Resurrection day) कहते हैं । इस दिन दुनिया में सब कुछ ख़त्म हो जायेगा और कुछ नहीं बचेगा | इस
दिन सभी मुर्दा लोगों की रूहों को सशरीर उनकी कब्र से निकाला जायेगा, और सारी रुहें अल्लाह के
सामने इकट्ठी हो जाएँगी । उस दिन हर रूह का हिसाब-किताब होगा, उसके हर छोटे से छोटे और बड़े से
बड़े कर्म की जांच की जाएगी, और सब कुछ मिलाकर यदि अंत में निष्कर्ष यह निकलता है कि रूह ने
अच्छे कर्म ज़्यादा किये हैं, तो उस 'नेक' रूह को अल्लाह स्वर्ग में जाने की अनुमति देगा | यदि रूह ने
पुण्य के मुकाबले पाप कर्म ज़्यादा किये हैं तो उस 'दुष्ट' रूह को अल्लाह नरक में डाल देता है।
इस्लाम धर्म के ग्रन्थ, जैसे कुरान और हदीस में यह कहा गया है कि एक बार स्वर्ग में जाने के बाद
व्यक्ति अनंत काल के लिए स्वर्ग में ही रहता है, इसी प्रकार नरक में जाने वाला व्यक्ति भी अनंत काल
के लिए नरक में ही रहेगा | अगर अल्लाह चाहे तो किसी रूह को नरक में से निकालकर स्वर्ग में जाने की
अनुमति दे सकता है।
मरने के तुरंत बाद से लेकर क़यामत का दिन आने तक के बीच का जो समय होता है उसे इस्लाम धर्म में
'बरज़ख़' कहा गया है। यानी 'बरज़ख़' के दौरान रूह कब्र में ही रहती है और क़यामत का दिन आने का
इंतज़ार करती है । बरज़ख में रूह के साथ क्या होता है इसके बारे में भी स्पष्ट वर्णन मिलता है।
वैसे तो हिसाब किताब कयामत वाले दिन होगा, लेकिन हर रूह को इस बात का संकेत बरज़ख़ में ही
मिल जाता है कि उसे नरक मिलेगा या स्वर्ग | अल्लाह के फ़रिश्ते कब्र में आकर हर रूह को पहले ही यह
बता देते हैं उस रूह ने कैसे कर्म किये हैं कैसे नहीं । नेक रूह को बरज़ख़ में भी स्वर्ग जैसा माहौल मिलता
है और दुष्ट रूह को नरक जैसा | हालांकि यह भी कहा गया है कि बरज़ख़ में दुष्ट रूह को मिलने वाली
सज़ा नरक की असली सज़ा का एक छोटा प्रारूप होती है। इसी प्रकार बरज़ख़ में नेक रूह को मिलने
वाला इनाम स्वर्ग के असली इनाम का एक छोटा प्रारूप होता है।मरने के बाद क्या होता है, कब्र में क्या सज़ाएं/इनाम मिलते हैं, क़यामत के बाद नरक/स्वर्ग में क्या
होता है इसका विस्तृत वर्णन कुरआन और हदीस के अंदर इस प्रकार दिया गया है:
जब एक नेक व्यक्ति मरणासन्न होता है तो उस समय उसके पास स्वर्ग से अनेकों फ़रिश्ते आते हैं | उन
फरिश्तों का चेहरा सूर्य की भाँती चमक रहा होता है । वह फ़रिश्ते अपने साथ स्वर्ग से एक कफ़न लेकर
आते हैं और स्वर्ग का इत्र भी साथ लाते हैं, और उस व्यक्ति के पास आकर बैठते हैं | फिर मौत का
फरिश्ता उस व्यक्ति के सर के पास आकर बैठता है और उससे कहता है, "हे नेक आत्मा(रूह), तू अल्लाह
की दया और अल्लाह के दिए हुए आनंद को लेने के लिए बाहर आ |"
यह सुनकर उस व्यक्ति के शरीर से उसकी रूह इस प्रकार बाहर निकलती है जैसे एक गिलास में से पानी
बहता है, (इसका तात्पर्य यह है कि नेक व्यक्ति की रूह को शरीर में से निकलते वक़्त ज़रा सी भी पीड़ा,
दुःख या किसी प्रकार की कोई परेशानी नहीं होती)। जैसे ही वह रूह मौत के फ़रिश्ते के हाथ में आती है,
बाकी फ़रिश्ते उससे उस रूह को लेकर कफ़न में लेटा देते हैं और उसपर इत्र छिड़कते हैं। उस कफ़न में से
कस्तूरी की ऐसी मनोहर सुगंध आती है जो पृथ्वी पर कहीं नहीं होती।
फिर उस कफ़न को लेकर फ़रिश्ते आसमान (स्वर्ग) की ओर ऊपर बढ़ते हैं | उस रूह को देखकर
आसमान में मौजूद बाकी फ़रिश्ते पूछते हैं कि "यह नेक रूह किसकी है ?"
इसके जवाब में कफ़न को उठाये हए फ़रिश्ते कहते हैं, " यह फलां फलां व्यक्ति की औलाद है फलां फलां
जगह से इसे लेकर आये हैं।" उस रूह का विवरण देते हुए फ़रिश्ते उन सभी अच्छे नामों से उसे पुकारते
हैं जिससे उसे पृथ्वी पर लोग बुलाया करते थे।
फ़रिश्ते उस रूह को लेकर प्रथम स्वर्ग के पास पहुँचते हैं और स्वर्ग का दरवाज़ा खोलने के लिए कहते हैं।
उस नेक रूह के लिए स्वर्ग का दरवाज़ा खोल दिया जाता है । फिर वह फ़रिश्ते उस रूह को और ऊपर
दूसरे स्वर्ग की ओर ले जाते हैं । प्रथम स्वर्ग में मौजूद फ़रिश्ते भी उस नेक रूह को देखकर उसका
स्वागत करते हैं, उसे बधाई देते हैं खुशखबरियाँ सुनाते हैं और उसे दूसरे स्वर्ग के दरवाजे तक छोड़ने के
लिए जाते हैं | फिर दूसरे स्वर्ग का दरवाज़ा खुलता है । इस 
प्रकार उस रूह को फ़रिश्ते सातवें स्वर्ग तक
ले जाते हैं जहाँ अल्लाह होता है | उस नेक रूह के लिए अल्लाह कहता है, " मेरे इस दास का नाम
इल्लियीन में दर्ज करके इसे वापिस पृथ्वी पर भेज दिया जाए | मैंने इसे पृथ्वी पर ही पैदा किया था,
इसीलिए इसे वहीं वापिस भेज दो और अंत में मैं इसे वहीं से वापिस अपने पास बुलाऊंगा |" सबसे ऊंचे
स्वर्ग में रखी जाने वाली पुस्तक को "इल्लियीन" कहा गया है।


फिर उस रूह को वापिस पृथ्वी पर ले जाया जाता है, और जिस कब्र में उसके परिवार वालों ने उसके मुर्दा
शरीर को दफनाया होता है उसी में लेटा दिया जाता है।
इस तरह एक नेक रूह को शरीर से निकालकर सातवें आसमान की सैर कराकर वापिस कब्र
में भेजे जाने तक के सफर के दौरान उसे सभी फ़रिश्ते बधाई देते हैं और खुशखबरियाँ सुनाते
हैं | जब वह 'नेक' रूह कब्र में पहुँचती है तो आसमान से दो फ़रिश्ते आते हैं जिनका नाम होता है
'मुनकर' और 'नाकिर' | यह दोनों फ़रिश्ते उस रूह से तीन सवाल पूछते हैं:
1. तुम्हारा मालिक कौन है?
2. तुम्हारा धर्म क्या है?
3. तुम इस व्यक्ति (हज़रत मुहम्मद) के बारे में क्या जानते हो?
नेक रूह इन तीन सवालों के यह जवाब देती है:
1.अल्लाह मेरा मालिक है।
2. इस्लाम मेरा धर्म है।
3. वे अल्लाह के भेजे हए पैगम्बर हैं।
इन तीनों सवालों के सही जवाब सुनकर वो फ़रिश्ते उस रूह से पूछते हैं कि उसे ये सारे जवाब कहाँ से
पता चले ? इसके जवाब में नेक रूह जवाब देती है कि “उसने अल्लाह की किताब (करान) पढ़ी और
उसपर विश्वास किया ।"
यह बातें सुनकर ऊपर आसमान से एक आवाज़ आती है, "मेरे इस दास ने सच बोला है, इसके लिए स्वर्ग
से बिस्तर और कपड़े लाये जाएँ, और इसे स्वर्ग का दरवाज़ा खोलकर दिखाया जाए |"
पहले उस रूह के बायीं ओर एक बार नरक का दरवाज़ा खोलकर उसे दिखाया जाता है जिसे खोलते ही
उसमें से दुर्गन्ध और झुलसा देने वाली आग निकलती है । इसे दिखाकर फ़रिश्ते रूह से कहते हैं, "यदि
तुमने इन सवालों के सही जवाब ना दिए होते तो तुम्हें ये नसीब होता |"
फिर नरक के दरवाजे को तुरंत बंद करके रूह की दायीं ओर स्वर्ग का दरवाज़ा खोलकर फ़रिश्ते कहते हैं,
"लेकिन अब तुम्हें नरक जाने से अल्लाह ने बचा लिया है और अब ये स्वर्ग तुम्हें नसीब होगा |"





फिर उस रूह की कब्र को अनंत गुना फैला दिया जाता है (बड़ा कर दिया जाता है), और उसमें स्वर्ग की
महक आने लगती है। फिर उस रूह के पास एक दमकते हुए चेहरे का व्यक्ति आता है जिसे देख कर रूह
कहती है, "तुम कौन हो, तुम्हें देखकर लगता है तुम कोई खुशखबरी लाये हो!" फिर वह व्यक्ति जवाब
देता है कि मैं तुम्हारे अच्छे कर्म है, जिसकी वजह से तुम्हे ये आनंद प्राप्त हो रहा है।
जब रूह यह सुनती है तो खुशी के मारे छटपटा उठती है और अल्लाह से दुआ करती है, "हे अल्लाह,
क़यामत का दिन जल्द से जल्द ले आ ताकि में अपने परिवार के साथ स्वर्ग का आनंद ले सकूँ |"
यहाँ ध्यान देने वाली बात यह है कि कब्र में नेक रूह को स्वर्ग की एक झलक मात्र ही दिखाई जाती है,
लेकिन स्वर्ग में जाने का असली मौका उसे क़यामत के दिन ही मिलता है।

